    private void popuniGlumce() {

        try {
            ArrayList<Glumac> glumci = ClientController.getInstance().getAllGlumac();

            cmbGlumac.removeAllItems();

            for (Glumac glumac : glumci) {
                cmbGlumac.addItem(glumac);
            }

        } catch (Exception ex) {
            Logger.getLogger(FormNoviFilm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
