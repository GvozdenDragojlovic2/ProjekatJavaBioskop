    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        try {
            if (txtNazivFilma.getText().isEmpty() || txtZanr.getText().isEmpty()
                    || txtOpisFilma.getText().isEmpty()
                    || txtReziser.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
                return;
            }

            String nazivFilma = txtNazivFilma.getText();
            String zanr = txtZanr.getText();
            String opisFilma = txtOpisFilma.getText();
            String reziser = txtReziser.getText();
            
            TableModelUloge tm = (TableModelUloge) tblUloge.getModel();

            Film f = new Film(null, nazivFilma, zanr, opisFilma, reziser, tm.getLista());

            ClientController.getInstance().addFilm(f);
            MainForm mf = (MainForm) getParent();
            mf.popuniFilmove();
            JOptionPane.showMessageDialog(this, "Uspesno dodat film.");
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnDodajActionPerformed

