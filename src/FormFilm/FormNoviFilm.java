    private void btnDodajUloguActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajUloguActionPerformed

        Glumac glumac = (Glumac) cmbGlumac.getSelectedItem();
        String nazivUloge = txtNazivUloge.getText();
        String opisUloge = txtOpisUloge.getText();

        Uloga u = new Uloga(null, -1, nazivUloge, opisUloge, glumac);

        TableModelUloge tm = (TableModelUloge) tblUloge.getModel();

        if (tm.postojiNaziv(nazivUloge)) {
            JOptionPane.showMessageDialog(this, "Vec postoji uloga sa tim nazivom!");
            return;
        }

        if (tm.postojiOpis(opisUloge)) {
            JOptionPane.showMessageDialog(this, "Vec postoji uloga sa tim opisom!");
            return;
        }
        
        tm.dodajUlogu(u);

    }//GEN-LAST:event_btnDodajUloguActionPerformed

    private void btnObrisiUloguActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiUloguActionPerformed

        int row = tblUloge.getSelectedRow();

        if (row >= 0) {
            TableModelUloge tm = (TableModelUloge) tblUloge.getModel();
            tm.obrisiUlogu(row);
        }

    }//GEN-LAST:event_btnObrisiUloguActionPerformed

